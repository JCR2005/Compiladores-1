    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        nombre = "";
        while (nombre == null || nombre.isEmpty()) {
            nombre = JOptionPane.showInputDialog(null, "Ingrese el nombre de su pagina:", "Minimal React", JOptionPane.INFORMATION_MESSAGE);

            if (nombre == null) {
                JOptionPane.showMessageDialog(null, "Se ha cancelado la creaci√≥n de su pagina", "Minimal React", JOptionPane.CANCEL_OPTION);
                return;
            }

            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El nombre de la pagina es requerido.", "Minimal React", JOptionPane.WARNING_MESSAGE);
            }
        }
        if (agregar.agregarPagina(jLabel1.getText() + "," + nombre)) {
            JOptionPane.showMessageDialog(null, "Su pagina a sido agregada", agregar.response(), JOptionPane.INFORMATION_MESSAGE);
            mostrarPaginas();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


