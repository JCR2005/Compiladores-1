    public void mostrarPaginas() {
        panelBotonesPaginas.removeAll();
        ArrayList<String> paginas = obtener.obtenerPaginasSitio(jLabel1.getText());

        for (int i = 0; i < paginas.size(); i++) {
            JButton boton = new JButton();
            boton.setText(paginas.get(i));

            boton.setBackground(Color.decode("#2c495b"));
            boton.setForeground(Color.decode("#FDF7E7"));
            boton.setFont(new Font("P052", Font.BOLD | Font.ITALIC, 24));
            boton.setHorizontalAlignment(SwingConstants.CENTER);
            boton.setVerticalAlignment(SwingConstants.CENTER);

            boton.setBorderPainted(false);
            boton.setFocusPainted(false);
            boton.setContentAreaFilled(true);

            boton.setBounds(0, (int) ((alto * 0.06)) * (i), (int) ((ancho * 0.2)), (int) ((alto * 0.05)));
            String nombre = boton.getText();

            boton.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    System.out.println("hoal");
                    panelEdicionPAgina(nombre);

                }
            });
            panelBotonesPaginas.add(boton);
            int alturaTotal = (int) (alto * 0.06) * paginas.size();
            panelBotonesPaginas.setPreferredSize(new Dimension((int) (ancho * 0.19), alturaTotal));

            panelBotonesPaginas.revalidate();
            panelBotonesPaginas.repaint();
            panelBotonesPaginas.updateUI();
        }

    }

